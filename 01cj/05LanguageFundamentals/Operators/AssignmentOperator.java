class AssignmentOperator{
    
    //unary operator
    public static void main(String[] args) {
        int a;
        a = 10;  //simple assignment
        System.out.println(a);
        
        //compound assignment operator
        a += 10;  //a= a+10
        System.out.println(a);
        a -= 10;  //a= a+10
        System.out.println(a);
        
        
    }
}