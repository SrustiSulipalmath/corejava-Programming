class Stringdt 
{
	public static void main(String[] args)      //java.lang.String   java.lang package same way it is added in bytecode
	{
		String s1 = "Srusti";               //java.lang.String       java.lang package
		String s2 = new String("GSM");
		System.out.println(s1+" "+s2);
		System.out.println("REVA");        //alphabets      without variable displaying text
		System.out.println(123);           //numbers
		System.out.println("@#$%");        //specialcharacters
		System.out.println("abc123@#$");   //alphanumericstring
		System.out.println("");            //empty doublequotes
	}
}
